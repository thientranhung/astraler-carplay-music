func main() {
	if len(os.Args) > 1 {
		switch os.Args[1] {
		case "init":
			cmdInit(os.Args[2:])
			return
		case "setup":
			cmdSetup(os.Args[2:])
			return
		case "tts":
			cmdTTS(os.Args[2:])
			return
		case "reset":
			cmdReset(os.Args[2:])
			return
		case "clean":
			cmdClean(os.Args[2:])
			return
		case "doctor", "--doctor":
			fmt.Printf("astra-carplay-music %s — doctor\n\n", version)
			doctor()
			return
		case "help", "--help", "-h":
			printHelp()
			return
		case "version", "--version":
			fmt.Printf("astra-carplay-music %s\n", version)
			return
		default:
			if !strings.HasPrefix(os.Args[1], "-") {
				fmt.Fprintf(os.Stderr, "Error: unknown subcommand '%s'\n\n", os.Args[1])
				fmt.Fprintf(os.Stderr, "Run 'astra-carplay-music --help' for usage.\n")
				os.Exit(2)
			}
		}
	}

	bgmVolume   := flag.Float64("bgm-volume", -3, "Background music volume in dB (default: -3)")
	ttsVolume   := flag.Float64("tts-volume", 0, "TTS voice volume in dB (default: 0)")
	delay       := flag.Float64("delay", 0.5, "Seconds of background music before TTS starts (default: 0.5)")
	duration    := flag.Float64("duration", 0, "Trim output to this duration in seconds (default: full length)")
	fadeOut     := flag.Float64("fade-out", 0, "Fade out duration in seconds at end (default: none)")
	showVersion := flag.Bool("version", false, "Show version")
	showDoctor  := flag.Bool("doctor", false, "Check required dependencies")

	flag.Usage = printHelp
	flag.Parse()

	if *showVersion {
		fmt.Printf("astra-carplay-music %s\n", version)
		os.Exit(0)
	}
	if *showDoctor {
		fmt.Printf("astra-carplay-music %s — doctor\n\n", version)
		doctor()
		os.Exit(0)
	}

	if err := runMix(*bgmVolume, *ttsVolume, int(*delay*1000), *duration, *fadeOut); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %s\n", err)
		os.Exit(1)
	}
}

