// ── clean subcommand ──────────────────────────────────────────────────────────

func cleanDir(dir string) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		fmt.Fprintf(os.Stderr, "  skip %s/ (not found)\n", dir)
		return
	}
	count := 0
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		ext := strings.ToLower(filepath.Ext(e.Name()))
		for _, ae := range audioExts {
			if ext == ae {
				_ = os.Remove(filepath.Join(dir, e.Name()))
				count++
				break
			}
		}
	}
	fmt.Fprintf(os.Stderr, "  ✓ %s/  (%d file(s) removed)\n", dir, count)
}

func cmdClean(args []string) {
	fs := flag.NewFlagSet("clean", flag.ExitOnError)
	doTTS    := fs.Bool("tts", false, fmt.Sprintf("Remove audio files in %s/", ttsDir))
	doBG     := fs.Bool("bg", false, fmt.Sprintf("Remove audio files in %s/", bgmDir))
	doOutput := fs.Bool("output", false, fmt.Sprintf("Remove audio files in %s/", outputDir))
	force    := fs.Bool("force", false, "Skip confirmation prompt")
	fs.Usage = func() {
		fmt.Fprintf(os.Stderr, "Usage: astra-carplay-music clean [options]\n\n")
		fmt.Fprintf(os.Stderr, "Remove audio files from the specified directories.\n")
		fmt.Fprintf(os.Stderr, "No directory flag = clean all three directories.\n\n")
		fmt.Fprintf(os.Stderr, "Examples:\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music clean --force          # remove all, no prompt\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music clean --tts            # only %s/\n", ttsDir)
		fmt.Fprintf(os.Stderr, "  astra-carplay-music clean --output         # only %s/\n", outputDir)
		fmt.Fprintf(os.Stderr, "  astra-carplay-music clean --tts --output --force\n\n")
		fmt.Fprintf(os.Stderr, "Options:\n")
		fs.PrintDefaults()
	}
	_ = fs.Parse(args)

	all := !*doTTS && !*doBG && !*doOutput

	var targets []string
	if *doTTS || all   { targets = append(targets, ttsDir) }
	if *doBG || all    { targets = append(targets, bgmDir) }
	if *doOutput || all { targets = append(targets, outputDir) }

	if !*force {
		fmt.Fprintf(os.Stderr, "This will delete all audio files in: %s\n", strings.Join(targets, ", "))
		fmt.Fprintf(os.Stderr, "Continue? [y/N] ")
		var answer string
		fmt.Scanln(&answer)
		if strings.ToLower(strings.TrimSpace(answer)) != "y" {
			fmt.Fprintln(os.Stderr, "Aborted.")
			os.Exit(0)
		}
	}

	fmt.Println()
	for _, t := range targets {
		cleanDir(t)
	}
	fmt.Println()
}

// ── mix (default) ─────────────────────────────────────────────────────────────

func findAudio(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("cannot read directory '%s': %w", dir, err)
	}
	var files []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		ext := strings.ToLower(filepath.Ext(e.Name()))
		for _, ae := range audioExts {
			if ext == ae {
				files = append(files, filepath.Join(dir, e.Name()))
				break
			}
		}
	}
	return files, nil
}

func mix(ttsPath, bgPath, outPath string, bgmVolume, ttsVolume float64, delayMs int, duration, fadeOut float64) error {
	mixChain := "amix=inputs=2:duration=first:dropout_transition=0"
	if fadeOut > 0 && duration > 0 {
		fadeStart := duration - fadeOut
		mixChain += fmt.Sprintf(",afade=t=out:st=%.3f:d=%.3f", fadeStart, fadeOut)
	}
	filter := fmt.Sprintf(
		"[0]volume=%.1fdB[bg];[1]volume=%.1fdB,adelay=%d|%d[tts];[bg][tts]%s[out]",
		bgmVolume, ttsVolume, delayMs, delayMs, mixChain,
	)
	args := []string{"-y", "-i", bgPath, "-i", ttsPath, "-filter_complex", filter, "-map", "[out]", "-b:a", "320k"}
	if duration > 0 {
		args = append(args, "-t", fmt.Sprintf("%.3f", duration))
	}
	args = append(args, outPath)

	if out, err := exec.Command("ffmpeg", args...).CombinedOutput(); err != nil {
		return fmt.Errorf("ffmpeg error: %s", string(out))
	}
	return nil
}

func doctor() {
	type check struct {
		name, command string
		args          []string
		install       string
	}
	checks := []check{
		{"ffmpeg", "ffmpeg", []string{"-version"}, "brew install ffmpeg"},
	}

	ok := true
	for _, c := range checks {
		out, err := exec.Command(c.command, c.args...).Output()
		if err != nil {
			fmt.Printf("  ✗ %-10s not found  →  %s\n", c.name, c.install)
			ok = false
			continue
		}
		firstLine := strings.SplitN(strings.TrimSpace(string(out)), "\n", 2)[0]
		fmt.Printf("  ✓ %-10s %s\n", c.name, firstLine)
	}

	fmt.Println()
	if ok {
		fmt.Println("All dependencies OK. Tool is ready.")
	} else {
		fmt.Fprintln(os.Stderr, "Missing dependencies. Install them and run again.")
		os.Exit(1)
	}
}

func runMix(bgmVolume, ttsVolume float64, delayMs int, duration, fadeOut float64) error {
	for _, dir := range []string{ttsDir, bgmDir} {
		if _, err := os.Stat(dir); os.IsNotExist(err) {
			return fmt.Errorf("directory '%s' not found. Run 'astra-carplay-music init' first", dir)
		}
	}

	ttsFiles, err := findAudio(ttsDir)
	if err != nil {
		return err
	}
	bgFiles, err := findAudio(bgmDir)
	if err != nil {
		return err
	}

	if len(ttsFiles) == 0 {
		return fmt.Errorf("no audio files found in '%s/' — run 'tts' command first. Supported: .mp3, .wav, .m4a", ttsDir)
	}
	if len(bgFiles) == 0 {
		return fmt.Errorf("no audio files found in '%s/'. Add background music (.mp3, .wav, .m4a)", bgmDir)
	}

	if err := os.MkdirAll(outputDir, 0755); err != nil {
		return fmt.Errorf("cannot create output directory: %w", err)
	}

	total := len(ttsFiles) * len(bgFiles)
	durStr := "full"
	if duration > 0 {
		durStr = fmt.Sprintf("%.1fs", duration)
	}
	fadeStr := "none"
	if fadeOut > 0 {
		fadeStr = fmt.Sprintf("%.1fs", fadeOut)
	}
	fmt.Fprintf(os.Stderr, "%d TTS × %d BG = %d file(s)  [bgm %+.0fdB | tts %+.0fdB | delay %.1fs | duration %s | fadeout %s]\n\n",
		len(ttsFiles), len(bgFiles), total, bgmVolume, ttsVolume, float64(delayMs)/1000, durStr, fadeStr)

	for _, bg := range bgFiles {
		bgName := strings.TrimSuffix(filepath.Base(bg), filepath.Ext(bg))
		for _, tts := range ttsFiles {
			ttsName := strings.TrimSuffix(filepath.Base(tts), filepath.Ext(tts))
			out := filepath.Join(outputDir, ttsName+"_"+bgName+".mp3")
			if err := mix(tts, bg, out, bgmVolume, ttsVolume, delayMs, duration, fadeOut); err != nil {
				return fmt.Errorf("%s: %w", out, err)
			}
			fmt.Printf("  ✓ %s\n", filepath.Base(out))
		}
	}

	fmt.Fprintf(os.Stderr, "\nDone. %d file(s) in '%s/'\n", total, outputDir)
	return nil
}

