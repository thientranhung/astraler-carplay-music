func cmdSetup(args []string) {
	fs := flag.NewFlagSet("setup", flag.ExitOnError)
	apiKey  := fs.String("api-key", "", "ElevenLabs API key")
	voiceID := fs.String("voice-id", "", "ElevenLabs Voice ID")
	fs.Usage = func() {
		fmt.Fprintf(os.Stderr, "Usage: astra-carplay-music setup [options]\n\n")
		fmt.Fprintf(os.Stderr, "Save ElevenLabs credentials to %s\n\n", configFile())
		fmt.Fprintf(os.Stderr, "Run without flags to show current config.\n\n")
		fmt.Fprintf(os.Stderr, "Examples:\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music setup --api-key sk_...\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music setup --voice-id abc123\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music setup\n\n")
		fmt.Fprintf(os.Stderr, "Options:\n")
		fs.PrintDefaults()
	}
	_ = fs.Parse(args)

	cfg := loadConfig()

	if *apiKey == "" && *voiceID == "" {
		fmt.Fprintf(os.Stderr, "Config: %s\n\n", configFile())
		if cfg.ElevenLabsAPIKey != "" {
			k := cfg.ElevenLabsAPIKey
			masked := k[:min(4, len(k))] + "****"
			fmt.Fprintf(os.Stderr, "  api-key  : %s\n", masked)
		} else {
			fmt.Fprintf(os.Stderr, "  api-key  : (not set)\n")
		}
		v := cfg.ElevenLabsVoiceID
		if v == "" {
			v = fmt.Sprintf("(default: %s)", defaultVoice)
		}
		fmt.Fprintf(os.Stderr, "  voice-id : %s\n\n", v)
		fmt.Fprintf(os.Stderr, "Use --api-key and --voice-id to update.\n")
		return
	}

	if *apiKey != "" {
		cfg.ElevenLabsAPIKey = *apiKey
		fmt.Fprintf(os.Stderr, "Warning: API key passed via flag may be saved in your shell history.\n")
		fmt.Fprintf(os.Stderr, "         Consider running: history -d $(history 1) to remove it.\n\n")
	}
	if *voiceID != "" {
		cfg.ElevenLabsVoiceID = *voiceID
	}
	if err := saveConfig(cfg); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %s\n", err)
		os.Exit(1)
	}
	fmt.Fprintf(os.Stderr, "✓ Config saved: %s\n", configFile())
}

// ── tts subcommand ────────────────────────────────────────────────────────────

func slugify(text string) string {
	text = strings.ToLower(strings.TrimSpace(text))
	var b strings.Builder
	prevDash := false
	for _, r := range text {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			b.WriteRune(r)
			prevDash = false
		} else if !prevDash {
			b.WriteRune('-')
			prevDash = true
		}
	}
	s := strings.Trim(b.String(), "-")
	if len(s) > 60 {
		s = s[:60]
	}
	return s
}

func generateTTS(text, voiceID, apiKey, outPath string, speed float64) error {
	payload, _ := json.Marshal(map[string]any{
		"text": text,
		"voice_settings": map[string]any{
			"speed": speed,
		},
	})
	url := fmt.Sprintf("https://api.elevenlabs.io/v1/text-to-speech/%s", voiceID)

	req, err := http.NewRequest("POST", url, bytes.NewReader(payload))
	if err != nil {
		return err
	}
	req.Header.Set("xi-api-key", apiKey)
	req.Header.Set("Content-Type", "application/json")

	resp, err := httpClient.Do(req)
	if err != nil {
		return fmt.Errorf("request failed: %w", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		body, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("API %d: %s", resp.StatusCode, strings.TrimSpace(string(body)))
	}

	if err := os.MkdirAll(filepath.Dir(outPath), 0755); err != nil {
		return err
	}
	f, err := os.Create(outPath)
	if err != nil {
		return err
	}
	defer f.Close()
	_, err = io.Copy(f, resp.Body)
	return err
}

func cmdTTS(args []string) {
	fs := flag.NewFlagSet("tts", flag.ExitOnError)
	text    := fs.String("text", "", "Text to generate as TTS audio")
	file    := fs.String("file", "", "Text file — one line per clip, lines starting with # are skipped")
	voiceID := fs.String("voice-id", "", "ElevenLabs Voice ID (overrides config)")
	speed   := fs.Float64("speed", 0.9, "Speech speed: 0.7 (slow) to 1.2 (fast) (default: 0.9)")
	outDir  := fs.String("output-dir", ttsDir, "Directory to save generated TTS files")
	fs.Usage = func() {
		fmt.Fprintf(os.Stderr, "Usage: astra-carplay-music tts [options]\n\n")
		fmt.Fprintf(os.Stderr, "Generate TTS audio via ElevenLabs API and save to %s/\n\n", ttsDir)
		fmt.Fprintf(os.Stderr, "Requires an API key — run setup first:\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music setup --api-key <key>\n\n")
		fmt.Fprintf(os.Stderr, "Output filename format: {text-slug}_{voice-id}.mp3\n\n")
		fmt.Fprintf(os.Stderr, "Examples:\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music tts --text \"Welcome to BMW\"\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music tts --file scripts.txt\n")
		fmt.Fprintf(os.Stderr, "  astra-carplay-music tts --file scripts.txt --speed 1.1 --voice-id abc123\n\n")
		fmt.Fprintf(os.Stderr, "Options:\n")
		fs.PrintDefaults()
	}
	_ = fs.Parse(args)

	cfg := loadConfig()

	if *speed < 0.7 || *speed > 1.2 {
		fmt.Fprintln(os.Stderr, "Error: --speed must be between 0.7 and 1.2")
		os.Exit(2)
	}

	if cfg.ElevenLabsAPIKey == "" {
		fmt.Fprintln(os.Stderr, "Error: ElevenLabs API key not set.")
		fmt.Fprintln(os.Stderr, "Run: astra-carplay-music setup --api-key <key>")
		os.Exit(2)
	}

	voice := cfg.ElevenLabsVoiceID
	if voice == "" {
		voice = defaultVoice
	}
	if *voiceID != "" {
		voice = *voiceID
	}

	var lines []string
	switch {
	case *text != "":
		lines = []string{*text}
	case *file != "":
		f, err := os.Open(*file)
		if err != nil {
			fmt.Fprintf(os.Stderr, "Error: cannot open '%s': %s\n", *file, err)
			os.Exit(1)
		}
		defer f.Close()
		scanner := bufio.NewScanner(f)
		for scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line != "" && !strings.HasPrefix(line, "#") {
				lines = append(lines, line)
			}
		}
	default:
		fs.Usage()
		os.Exit(2)
	}

	if len(lines) == 0 {
		fmt.Fprintln(os.Stderr, "Error: no text lines found to generate.")
		os.Exit(2)
	}

	fmt.Fprintf(os.Stderr, "Generating %d TTS  [voice: %s | speed: %.2f]  →  %s/\n\n", len(lines), voice, *speed, *outDir)

	used := map[string]int{}
	ok, fail := 0, 0
	for _, line := range lines {
		slug := slugify(line)
		if slug == "" {
			slug = "tts"
		}
		idx := used[slug]
		used[slug]++

		name := fmt.Sprintf("%s_%s.mp3", slug, voice)
		if idx > 0 {
			name = fmt.Sprintf("%s_%s-%d.mp3", slug, voice, idx+1)
		}

		outPath := filepath.Join(*outDir, name)
		if err := generateTTS(line, voice, cfg.ElevenLabsAPIKey, outPath, *speed); err != nil {
			fmt.Fprintf(os.Stderr, "  ✗ %s: %s\n", name, err)
			fail++
			continue
		}
		fmt.Fprintf(os.Stderr, "  ✓ %s\n", name)
		ok++
	}

	fmt.Fprintf(os.Stderr, "\nDone. %d succeeded", ok)
	if fail > 0 {
		fmt.Fprintf(os.Stderr, ", %d failed", fail)
	}
	fmt.Fprintf(os.Stderr, "  →  %s/\n", *outDir)
	if fail > 0 {
		os.Exit(1)
	}
}

